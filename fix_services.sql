-- 1. إسقاط الجدول القديم الذي به نقص في الأعمدة
DROP TABLE IF EXISTS services;

-- 2. إنشاء الجدول الجديد بجميع الأعمدة الصحيحة والمطلوبة للموقع
CREATE TABLE services (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT UNIQUE,
    category TEXT,
    price NUMERIC,
    total_hours NUMERIC,
    payment_due_hours NUMERIC,
    validity_days INTEGER,
    total_reels INTEGER,
    type TEXT,
    deposit NUMERIC,
    description TEXT
);

